public class Positive_Negative {
      public static void main(String[] args) {
            int n1 = 5;
            int n2 = -5;
            check(n1);
            check(n2);
      }

      public static void check(int n){
            if(n>0){
                  System.out.println("Positive");
            }
            else{
                  System.out.println("Negative");
            }
      }
}
